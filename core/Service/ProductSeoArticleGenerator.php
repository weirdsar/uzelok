<?php

declare(strict_types=1);

namespace Uzelok\Core\Service;

use Uzelok\Core\Brand;

/**
 * Builds a unique Russian SEO article per product from title, brand, category and Ozon description.
 */
final class ProductSeoArticleGenerator
{
    /**
     * Plain text, paragraphs separated by double newlines (for <p> splitting).
     */
    public static function generate(array $product): string
    {
        $title = trim((string) ($product['title'] ?? 'Товар'));
        if ($title === '') {
            $title = 'Товар';
        }
        $brandEnum = Brand::tryFrom((string) ($product['brand_type'] ?? ''));
        $brandLabel = $brandEnum !== null ? $brandEnum->label() : 'УЗЕЛОК64';
        $desc = trim((string) preg_replace('/\s+/u', ' ', (string) ($product['description'] ?? '')));
        $category = trim((string) ($product['category'] ?? ''));
        $tl = mb_strtolower($title, 'UTF-8');

        $parts = [];
        $parts[] = self::paragraphIntro($title, $brandLabel, $category);
        $parts[] = self::paragraphUsage($tl, $title, $brandLabel, $brandEnum);
        $parts[] = self::paragraphWhyMatters($tl, $title, $brandLabel);
        if ($desc !== '' && mb_strlen($desc) > 50) {
            $parts[] = self::paragraphFromCard($title, $desc);
        }
        $parts[] = self::paragraphCta($title, $brandLabel);

        return implode("\n\n", array_values(array_filter(array_map(trim(...), $parts), static fn (string $p): bool => $p !== '')));
    }

    private static function paragraphIntro(string $title, string $brandLabel, string $category): string
    {
        $catHint = $category !== ''
            ? sprintf(' Категория на маркетплейсе: %s — это подсказка, в каких сценариях чаще всего выбирают такой аксессуар.', $category)
            : '';

        return sprintf(
            '«%s» от бренда %s — это не «ещё одна безделушка», а рабочий предмет, который решает конкретную бытовую или дорожную задачу: порядок, сохранность вещей и быстрый доступ к нужному без лишнего шума в салоне, в багажнике, в мастерской или в походе.%s Мы разобрали, кому этот товар действительно нужен, как его логично использовать и почему он окупается уже с первых поездок или рабочих смен.',
            $title,
            $brandLabel,
            $catHint
        );
    }

    private static function paragraphUsage(string $tl, string $title, string $brandLabel, ?Brand $brandEnum): string
    {
        if (str_contains($tl, 'якорь') || (str_contains($tl, 'парашют') && str_contains($tl, 'рыбалк'))) {
            return sprintf(
                'На воде якорь и оснастка используются там, где нужно удержать лодку или катер в точке лова или стоянки без постоянной работы мотором. Правильно подобранное изделие снижает расход топлива и шум, помогает стоять у перспективного места даже при ветре и слабом течении. «%s» подойдёт тем, кто рыбачит с плавсредства и хочет меньше «бороться» с позиционированием и больше времени уделять самой рыбалке. Перед применением сверьтесь с инструкцией производителя и условиями на вашем водоёме.',
                $title
            );
        }
        if (str_contains($tl, 'мешочек') && (str_contains($tl, 'ягод') || str_contains($tl, 'трав') || str_contains($tl, 'лекарствен'))) {
            return sprintf(
                'Такой мешочек удобен для бережного хранения сухих ягод, трав и мелких сыпучих продуктов дома: он помогает держать запасы отдельно, сохранять аромат и не смешивать разные партии. Его можно положить в кладовую, на кухню или взять в дорогу — текстиль «дышит» лучше плотного пластика. «%s» от %s ориентирован на практичное хранение без лишней тары.',
                $title,
                $brandLabel
            );
        }
        if (str_contains($tl, 'мешочек') && str_contains($tl, 'продукт')) {
            return sprintf(
                'Мешочек для сухих продуктов помогает на кухне и в кладовой держать крупы, орехи, специи и сыпучее в одном месте, не рассыпая по полкам. Это удобно для семей, которые закупают запас и хотят видеть содержимое с первого взгляда. «%s» можно комбинировать с банками и контейнерами: текстильная упаковка мягкая и не царапает мебель.',
                $title
            );
        }
        if (str_contains($tl, 'аптечк')) {
            return sprintf(
                'Автомобильная аптечка и расходники к ней должны лежать так, чтобы их было легко найти в стрессе — после мелкого пореза, укачивания или при остановке на обочине. Сумка под аптечку защищает содержимое от пыли и влаги в багажнике, а яркая маркировка экономит секунды. «%s» логично держать вместе с огнетушителем и светоотражающим жилетом: один «уголок безопасности» в машине.',
                $title
            );
        }
        if (str_contains($tl, 'компрессор')) {
            return sprintf(
                'Компрессор часто валяется в багажнике и царапается о инструменты. Отдельная сумка фиксирует шланг, кабель и сам агрегат, чтобы ничего не перекручивалось и не стучало при движении. «%s» уместна для тех, кто регулярно подкачивает колёса на даче, на трассе или помогает знакомым — вы тратите меньше времени на сборы и меньше нервов на поиск «где тот штуцер».',
                $title
            );
        }
        if (str_contains($tl, 'прикуриван') || str_contains($tl, 'проводов прикуриван')) {
            return sprintf(
                'Провода прикуривания должны быть чистыми, целыми и под рукой зимой и в межсезонье. Сумка исключает спутывание с другими вещами в бардачке и снижает риск повреждения изоляции. «%s» — для водителей, которые хотят один раз уложить комплект аккуратно и больше не возвращаться к хаосу в багажнике.',
                $title
            );
        }
        if (str_contains($tl, 'буксировочн') || str_contains($tl, 'строп')) {
            return sprintf(
                'Буксировочная стропа или трос — предмет редкого, но критического применения: она должна быть сухой, целой и быстро доступной. Текстильная сумка защищает от грязи и абразива в багажнике и не даёт стропе «расползтись» по коврику. «%s» имеет смысл держать рядом с домкратом и колёсным ключом — базовый набор для непредвиденной ситуации на дороге.',
                $title
            );
        }
        if (str_contains($tl, 'домкрат')) {
            return sprintf(
                'Домкрат, особенно после использования, бывает грязным. Отдельная сумка отделяет его от ковролина и вещей в багажнике, упрощает переноску и хранение в гараже. «%s» подойдёт автовладельцам, которые один раз наводят порядок в «аварийном комплекте» и хотят, чтобы всё лежало предсказуемо.',
                $title
            );
        }
        if (str_contains($tl, 'автоэлектрик') || (str_contains($tl, 'электрик') && str_contains($tl, 'сумк'))) {
            return sprintf(
                'Предохранители, клеммы, лампы и мелкий электротекстиль теряются быстрее всего. Органайзер под автоэлектрику позволяет разнести мелочь по отделениям и не рыть ящик в темноте у фонаря. «%s» полезен тем, кто сам обслуживает проводку или возит с собой расходники для подстраховки в поездке.',
                $title
            );
        }
        if (str_contains($tl, 'резцов') && str_contains($tl, 'дерев')) {
            return sprintf(
                'Резцы по дереву требуют аккуратного хранения: удары о металл портят заточку. Сумка-скрутка с карманами фиксирует инструмент по местам и снижает риск сколов. «%s» ориентирована на столярку, ремонт и творческие работы там, где важно быстро разложить набор и так же быстро убрать, не теряя мелкие детали.',
                $title
            );
        }
        if (str_contains($tl, 'скрутк') && str_contains($tl, 'авто')) {
            return sprintf(
                'Скрутка для автоинструмента экономит место в багажнике: ключи, отвёртки и мелочь не гремят и не рассыпаются. Ткань с пропиткой переносит перепады температур лучше жёсткого ящика. «%s» выбирают те, кто ценит порядок в машине и не хочет тратить время на сборы перед каждой поездкой.',
                $title
            );
        }
        if (str_contains($tl, 'скрутк') || (str_contains($tl, 'органайзер') && str_contains($tl, 'авто'))) {
            return sprintf(
                'Скрутка-органайзер удобна в багажнике, в гараже и на даче: инструмент виден целиком после раскладки, а в свернутом виде занимает мало места. «%s» от %s подходит для ручного инструмента и расходников среднего размера — когда ящик избыточен, а «просто в пакет» уже неудобно.',
                $title,
                $brandLabel
            );
        }
        if (str_contains($tl, 'сумк') && str_contains($tl, 'инструмент')) {
            return sprintf(
                'Универсальная сумка под инструмент помогает держать набор в одном месте: в квартире при мелком ремонте, в машине для дорожных ситуаций или в мастерской. Отдельные отделения сокращают время поиска нужной отвёртки или ключа. «%s» — выбор для тех, кто устал от «всё в одной куче» и хочет предсказуемый порядок без тяжёлого металлического ящика на каждый выезд.',
                $title
            );
        }

        return self::paragraphUsageByBrand($title, $brandEnum);
    }

    private static function paragraphUsageByBrand(string $title, ?Brand $brandEnum): string
    {
        if ($brandEnum === Brand::BUY) {
            return sprintf(
                'В линейке автоаксессуаров важны компактность, прочный материал и понятное назначение. «%s» помогает не смешивать дорожные мелочи с бытовыми и держать в багажнике или салоне структуру, к которой привыкаешь за одну-две поездки. Это снижает стресс при поломке, ночной остановке или длительном путешествии, когда каждая минута на поиски лишняя.',
                $title
            );
        }
        if ($brandEnum === Brand::VOLNA) {
            return sprintf(
                'Для охоты, рыбалки и туризма вещи часто влажные, грязные или объёмные — им нужна отдельная тара и порядок в рюкзаке или на борту. «%s» вписывается в сценарии, где важны прочность ткани и удобство переноски. Подберите место хранения так, чтобы к аксессуару был доступ и в тихую погоду, и в спешке перед выездом на рассвете.',
                $title
            );
        }

        $bl = $brandEnum !== null ? $brandEnum->label() : 'УЗЕЛОК64';

        return sprintf(
            'В хозяйстве и мастерской текстильные сумки и органайзеры экономят время: меньше поисков, меньше потерь мелочи, проще навести порядок в шкафу или в машине. «%s» от %s можно использовать там, где важна мобильность и бережное отношение к содержимому — от дачи до городской квартиры.',
            $title,
            $bl
        );
    }

    private static function paragraphWhyMatters(string $tl, string $title, string $brandLabel): string
    {
        $base = 'Порядок в вещах напрямую влияет на безопасность и комфорт: вы тратите меньше когнитивного ресурса на «где лежало», реже покупаете дубликаты потерянной мелочи и быстрее реагируете в нештатной ситуации. ';
        if (str_contains($tl, 'якорь') || str_contains($tl, 'строп') || str_contains($tl, 'аптечк')) {
            $base .= 'Для снаряжения, связанного с безопасностью, отдельное хранение — не каприз, а привычка подготовленного человека. ';
        }
        if (str_contains($tl, 'инструмент') || str_contains($tl, 'резц')) {
            $base .= 'Для инструмента аккуратное хранение продлевает срок службы режущих кромок и крепежа. ';
        }

        return $base . sprintf(
            '«%s» от бренда %s — часть такой системы: не обязательно идеальный минимализм, достаточно одного постоянного места и одного понятного аксессуара под конкретную задачу.',
            $title,
            $brandLabel
        );
    }

    private static function paragraphFromCard(string $title, string $desc): string
    {
        $excerpt = mb_strlen($desc) > 720 ? mb_substr($desc, 0, 717) . '…' : $desc;

        return sprintf(
            'Кратко о свойствах из официальной карточки товара (Ozon): %s Эти детали стоит сопоставить с вашим сценарием: частота поездок, климат, состав комплекта и личные привычки к хранению.',
            $excerpt
        );
    }

    private static function paragraphCta(string $title, string $brandLabel): string
    {
        return sprintf(
            'Если «%s» закрывает вашу задачу, оформите заказ на uzelok64.ru напрямую — так часто выгоднее, чем только через маркетплейс, особенно при опте и повторных покупках. Мы работаем с текстилем и швейным производством (бренд %s и линейка УЗЕЛОК64) и можем подсказать по размерам и совместимости. Нажмите «Заказать дешевле» на этой странице или напишите через контакты — ответим по срокам и доставке по РФ.',
            $title,
            $brandLabel
        );
    }
}
